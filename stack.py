#How to implement a stack using an array

stack = []

print("Stack before pushes:")
print((stack))

#Add stuff to the stack
stack.append('a')
stack.append('b')
stack.append('c')

print("\nStack after pushes:")
print(stack)

#Take stuff of the stack
print("\nElements in the stack:")
print(stack.pop())
print(stack.pop())
print(stack.pop())



